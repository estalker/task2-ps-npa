def industry_from_vpd_code(vpd_code: str | None) -> str:
    """
    По коду вида NN.NNN возвращает строку отрасли; иначе «—».
    """
    if not vpd_code:
        return "—"
    s = re.sub(r"\s+", "", str(vpd_code).strip())
    m = re.match(r"^(\d{2})\.\d{3}$", s)
    if not m:
        return "—"
    prefix = m.group(1)
    label = INDUSTRY_BY_PREFIX.get(prefix, "—")
    # UI/таблица: без префикса «19 » в ячейке отрасли.
    return re.sub(r"^\d{2}\s+", "", label).strip() if label != "—" else "—"
